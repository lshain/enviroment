package orz.lshain.mutithreadlib;

public class LT_Thread extends Thread
{
	@Override
	public void run( )
	{
		super.run( );
	}
	
	@Override
	public void interrupt( )
	{
		super.interrupt( );
	}
	
	@Override
	public boolean isInterrupted( )
	{
		return super.isInterrupted( );
	}
	
	@Override
	public synchronized void start( )
	{
		super.start( );
	}
}
